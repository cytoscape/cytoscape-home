import React from 'react'
import { useQuery } from "@tanstack/react-query"
import PropTypes from 'prop-types'
import { Button } from '@/components/base/Button'
import { TextSkeleton } from '@/components/base/Loading'
import { Card } from '@/components/results/Card'
import ReactMarkdown from '@/components/base/ReactMarkdown'
import { createAIOverviewQueryOptions } from '@/app/shared/queryOptions'
import { ChatBubbleLeftRightIcon } from '@heroicons/react/24/outline'
import { SparklesIcon } from '@heroicons/react/24/solid'
import { ExclamationTriangleIcon } from '@heroicons/react/20/solid'


export const AIOverviewCard = React.memo(({ searchText, onOpenChatbot }) => {
  const { data, error, isFetching } = useQuery(createAIOverviewQueryOptions(
    searchText,
    searchText?.trim().length > 0
  ))
  
  return (
    <Card
      id="card-ai"
      icon={<SparklesIcon className="h-8 w-8 fill-complement-500" />}
      title="AI Overview"
      url={`https://ollama.com/library/${data?.model ?? ''}`}
      caption={isFetching ? '' : `AI-generated by ${data?.model ?? 'UNKNOWN model'}`}
    >
      <div className="w-full min-h-64 max-h-128 p-2 text-sm text-gray-500 flex flex-col items-center justify-center">
        {isFetching && (
          <TextSkeleton showTitle={true} lines={[3, 7]} />
        )}
        {error && (
          <div className="w-full flex items-center justify-center text-red-800">
            <ExclamationTriangleIcon className="w-5 h-5 mr-2" />
            <span className="font-light">
              {error.message ? error.message : 'Unable to fetch AI insights'}
            </span>
          </div>
        )}
        {!isFetching && !error && data?.message?.content && (
          <div className="w-full flex flex-col items-end justify-center space-y-2">
            <div className="w-full min-h-32 max-h-64 p-2 text-sm text-gray-500 list-style-disc overflow-y-auto">
              <ReactMarkdown>
                {data.message.content}
              </ReactMarkdown>
            </div>
            <Button
              variant="solid"
              color="complement"
              onClick={() => onOpenChatbot(data)}
              className="inline-flex items-center m-1 pr-5"
            >
              <ChatBubbleLeftRightIcon
                aria-hidden="true"
                className="mr-2 size-7 text-white"
              />
              Chat with AI
            </Button>
          </div>
        )}
      </div>
    </Card>
  )
})
AIOverviewCard.displayName = 'AIOverviewCard'
AIOverviewCard.propTypes = {
  searchText: PropTypes.string.isRequired,
  onOpenChatbot: PropTypes.func.isRequired,
}

export default AIOverviewCard